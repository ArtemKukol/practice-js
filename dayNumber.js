const dayNumber = 3; //задайте номер дня

if (dayNumber === 1) {
   console.log('Понедельник');
}else if (dayNumber === 2) {
   console.log('Вторник');
}else if (dayNumber === 3) {
   console.log('Среда');
}else if (dayNumber === 4) {
   console.log('Четверг');
}else if (dayNumber === 5) {
   console.log('Пятница');
}else if (dayNumber === 6) {
   console.log('Суббота');
}else if (dayNumber === 7) {
   console.log('Воскресенье');
}else {
   console.log('Некорректный номер дня')
}